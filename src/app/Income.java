    @Override
    public void loadChart() {
        // Daftar bulan dalam urutan
        String[] bulanArr = {
            "Jan", "Feb", "March", "April", "May", "June", "July", "August", "Sept", "Oct", "Nov", "Dec"
        };

        // Map untuk menyimpan hasil dari database
        Map<String, Integer> dataBulan = new HashMap<>();

        String query = "SELECT MONTH(date) AS bulan, SUM(amount) AS total FROM transactions WHERE account_id = " + Session.id + " AND type='pemasukan' GROUP BY MONTH(date)";
        ResultSet rs = Database.executeQuery(query);
        try {
            while (rs.next()) {
                int index = rs.getInt("bulan") - 1; // 0-based index
                int total = rs.getInt("total");
                if (index >= 0 && index < 12) {
                    dataBulan.put(bulanArr[index], total);
                }
            }
            rs.close(); // Menutup database setelah mengambil data
        } catch (SQLException e) {
            e.printStackTrace();
        }
        
        DefaultCategoryDataset dataset = new DefaultCategoryDataset();
        for (String bulan : bulanArr) {
            int nilai = dataBulan.getOrDefault(bulan, 0);
            dataset.setValue(nilai, "Income", bulan);
        }

        // Membuat grafik garis
        JFreeChart lineChart = ChartFactory.createLineChart(
                "",
                "Month",
                "Amount (Rp)",
                dataset,
                PlotOrientation.VERTICAL,
                false,
                true,
                false
        );

        ChartPanel chartPanel = new ChartPanel(lineChart);
        chartJPanel.removeAll();
        chartJPanel.setLayout(new BorderLayout()); // WAJIB
        chartPanel.setPreferredSize(chartJPanel.getSize());
        chartJPanel.add(chartPanel, BorderLayout.CENTER);
        chartJPanel.revalidate();
        chartJPanel.repaint();

    }
    
    @Override
    public void loadTableData() {
        String searchText = searchField.getText().trim(); // Ambil teks pencarian

        // Ambil bulan dan tahun saat ini
        LocalDate currentDate = LocalDate.now();
        int currentMonth = currentDate.getMonthValue();
        int currentYear = currentDate.getYear();
        
        String query = "SELECT id, date, type, category, amount, note FROM transactions "
                             + "WHERE account_id = '" + Session.id + "' AND type = 'pemasukan'"
                             + "AND MONTH(date) = " + currentMonth + " "
                             + "AND YEAR(date) = " + currentYear;

        // Tambahkan kondisi pencarian jika searchText tidak kosong
        if (!searchText.isEmpty()) {
            query += " AND ("
                    + "id LIKE '%" + searchText + "%' OR "
                    + "date LIKE '%" + searchText + "%' OR "
                    + "category LIKE '%" + searchText + "%' OR "
                    + "amount LIKE '%" + searchText + "%' OR "
                    + "note LIKE '%" + searchText + "%')";
        }

        ResultSet rs = Database.executeQuery(query);
        DefaultTableModel model = (DefaultTableModel) jTable1.getModel();
        model.setRowCount(0); // Kosongkan tabel sebelum mengisi ulang

        try {
            while (rs.next()) {
                Object[] row = {
                    rs.getInt("id"),
                    rs.getDate("date"),
                    rs.getString("category"),
                    rs.getInt("amount"),
                    rs.getString("note")
                };
                model.addRow(row);
            }
            rs.close();
        } catch (SQLException e) {
            e.printStackTrace();
        }
    }
    
    @Override
    public void loadTotal() {
        try {
            String query = "SELECT SUM(amount) FROM transactions WHERE account_id = '" + Session.id + "' AND type = 'pemasukan' AND MONTH(date) = MONTH(CURRENT_DATE()) AND YEAR(date) = YEAR(CURRENT_DATE())";
            ResultSet rs = Database.executeQuery(query);

            if (rs.next()) {
                int totalIncome = rs.getInt(1);
                total.setText("Rp. " + String.format("%,d", totalIncome));
            }
            rs.close();
        } catch (Exception e) {
            e.printStackTrace();
        }
    }
    
